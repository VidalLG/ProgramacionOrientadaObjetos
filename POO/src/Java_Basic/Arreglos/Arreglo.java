package Java_Basic.Arreglos;

public class Arreglo {
    public static void main(String[] args) {

        int numeros [] = new int[5];

        numeros [0] = 52;
        numeros [1] = 4432;
        numeros [2] = 542;
        numeros [3] = 32;
        numeros [4] = 5342;

        for (int i = 0; i <= 4; i++){

            System.out.println("[" + numeros[i] + "]");
        }


    }

}
